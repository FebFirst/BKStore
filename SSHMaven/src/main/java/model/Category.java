package model;

public class Category {
	private int cataID;
	private String name;
	public int getCataID() {
		return cataID;
	}
	public void setCataID(int cataID) {
		this.cataID = cataID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
